package org.usfirst.frc.team5002.io.config.exception;

public class ConfigLexException extends Exception {
	
	public ConfigLexException() {
		  super();
	  }
	  public ConfigLexException(String message) {
		  super(message);
	  }
	  public ConfigLexException(String message, Throwable cause) {
		  super(message, cause);
	  }
	  public ConfigLexException(Throwable cause) {
		  super(cause);
	  }

}
